public class Java_Variable {
    public static void main(String[] args) {

        // Declare variable as type variableName = value;

        // Create variable and assign value
        int myNum1 = 15;

        // Create variable then assign value
        int myNum2;
        myNum2 = 15;

        // Create variable and assign and re-assign value

        int myNum3 = 15;
        myNum3 = 20; // myNum is now 20

        // single datatype multiple variable
        int myNum4 = 12, myNum5 = 23;

        // Note: Final Variables
        final int myNum6 = 15;
        // myNum6 = 20; // error
        System.out.println(myNum6);

        
        String name = "Sam";
        System.out.println("Good Morning " + name);

    }
}
