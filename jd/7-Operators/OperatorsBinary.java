
public class OperatorsBinary {

    public static void main(String[] args) {
        int a = 5;
        int b = 4;
        System.out.println("Add will be " + (a + b));
        System.out.println("Subtract will be " + (a - b));
        System.out.println("Product will be " + (a * b));
        System.out.println("Divide will be " + (a / b));
        System.out.println("Modulo will be " + (a % b));


    }
}
